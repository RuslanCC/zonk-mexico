// Блок справки для встраивания внизу игрового экрана.
// Кнопка-«summary» раскрывает вложенные разделы правил.
export function gameHelpHtml(gameKey) {
  const inner = HELP[gameKey];
  if (!inner) return '';
  return `
    <details class="acc acc--help" style="margin-top:18px">
      <summary>📖 Как играть — правила</summary>
      <div class="acc__body acc__body--help">${inner}</div>
    </details>`;
}
